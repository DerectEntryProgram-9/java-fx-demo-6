public class UnBoxingDemo {

//        char -> Character
//        boolean -> Boolean
//        short -> Short
//        int -> Integer
//        long -> Long
//        double -> Double
//        float -> Float

    Byte b = 10;

    /* Manual Unboxing */
    byte b2 = b.byteValue();

    /* Auto Unboxing */
    byte b3 = b;

    // b kiyanne memory location ekak. eka primitive value ekakata dala tyenne auto-unboxing una nisa
}
